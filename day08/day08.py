def get_input(filename):
    l = []
    with open(filename,'r') as f:
        n = 1
        for line in f:
            if n == 1:
                d = line.strip()
                n += 1

            else:
                l.append(line.strip())
    return (d, l)


#-------------------------------------------------------------------
#-------------------------------------------------------------------
def get_node_db(d):
    my_db = {}
    for n in d:
        name  = n[0 : 3]
        left  = n[7 : 10]
        right = n[12 : 15]
        my_db[name] = (left, right)
    return my_db


